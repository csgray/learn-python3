# Creates a string containing a tab and sets the value of "tabby_cat"  to that
tabby_cat = "\tI'm tabbed in."
# Creates a string containing a new line character and sets the value of "persian_cat" to that
persian_cat = "I'm split\non a line."
# Creates a string containing a couple of backslashes and sets the value of "backslash_cat" to that
backslash_cat = "I'm \\ a \\ cat."

# Creates a multi-ling string containing tabs and a new line character and sets the value of "fat_cat" to that
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''

# These output the values of each of the cats
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)